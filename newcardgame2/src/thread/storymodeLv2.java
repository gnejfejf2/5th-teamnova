package thread;

public class storymodeLv2 implements Runnable{

	@Override
	public void run() {
		System.out.println("바쿠라 : 오랜만이군 유우기");
		try {
			Thread.sleep(1500);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		System.out.println("유희 : 역시 천년링의 어둠의 의지는 사라지지 않았군");
		try {
			Thread.sleep(1500);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		System.out.println("죠이 : 저 녀석의 어둠의 인격이 아직 남아있다고?");
		try {
			Thread.sleep(1500);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		System.out.println("??? : 그게 무슨소리야?");
		try {
			Thread.sleep(1500);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		System.out.println("죠이 : 바쿠라가 가지고 있는 천년 아이템은 사악한 의지를 가지고있지");
		try {
			Thread.sleep(1500);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		System.out.println("죠이 : 그 사악한 의지가 지금의 마리크를 조종하고있는거야");
		try {
			Thread.sleep(1500);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		System.out.println("죠이 : 이 듀얼 뭔가 느낌이 좋지않군");
		try {
			Thread.sleep(1500);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		System.out.println("유희 : 아직도 내 천년 퍼즐을 노리고 있는거냐");
		try {
			Thread.sleep(1500);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		System.out.println("유희 : 자 와라 듀얼이다!");
		try {
			Thread.sleep(1500);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		
	}

}
